# 1. if
# O Python utiliza a instrução if para testar uma condição booleana e executar um bloco de código quando essa condição for verdadeira.

# 2. if-else
# Para oferecer uma rota alternativa quando a condição inicial não é atendida, a combinação if-else permite executar um bloco de código se a condição for falsa, garantindo fluxos de decisão completos.

# 3. if-elif-else
# Quando há múltiplas condições a serem verificadas em sequência, o elif (else if) possibilita testar cada condição até encontrar a primeira verdadeira, executando apenas o bloco correspondente e ignorando os demais.

# 4. Expressão condicional (ternária)
# O Python também suporta expressões condicionais de uma única linha, na forma:
# resultado = X if condição else Y
# Essa sintaxe oferece uma maneira concisa de atribuir valores com base em uma condição.

# 5. Condicionais aninhadas
# É possível colocar estruturas condicionais dentro de outras para criar hierarquias de decisão mais complexas, permitindo verificar subcondições somente se a condição externa for satisfeita.

# 6. match-case (padrões)
# A partir do Python 3.10, o match-case introduz o conceito de correspondência de padrões, comparando um valor contra diversos casos estruturados de forma semelhante a um switch, mas com suporte a padrões mais ricos e extração de dados.

